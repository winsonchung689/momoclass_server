package com.xue.entity.model;


public class AllCount {

    private Integer student_count;

    private Float total_amount;

    private Float left_amount;


    public Integer getStudent_count() {
        return student_count;
    }

    public void setStudent_count(Integer student_count) {
        this.student_count = student_count;
    }

    public Float getTotal_amount() {
        return total_amount;
    }

    public void setTotal_amount(Float total_amount) {
        this.total_amount = total_amount;
    }

    public Float getLeft_amount() {
        return left_amount;
    }

    public void setLeft_amount(Float left_amount) {
        this.left_amount = left_amount;
    }

    
}